#include "libft.h"

static void	ft_putnbr(int n, int fd)
{
	char	c;

	if (n > 0)
	{
		ft_putnbr(n / 10, fd);
		c = n % 10 + '0';
		write(fd, &c, 1);
	}
}

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else if (n < 0)
	{
		write(fd, "-", 1);
		ft_putnbr(-n, fd);
	}
	else if (n == 0)
		write(fd, "0", 1);
	else
		ft_putnbr(n, fd);
}
